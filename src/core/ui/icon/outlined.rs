use super::shared::BASE_CLASS;

fn new(html: &str, class: &str) -> Elem {
    let replaced: String = html.replace(
        "<svg ",
        &format!(
            "<svg stroke=\"currentColor\" class=\"{} {}\" ",
            BASE_CLASS,
            class.trim()
        ),
    );
    unsafe_text(&replaced)
}

