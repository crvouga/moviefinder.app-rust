fn new(html: &str, class: &str) -> Html {
    let replaced: String = html.replace(
        "<svg ",
        &format!(
            "<svg fill=\"currentColor\" class=\"{} {}\" ",
            BASE_CLASS,
            class.trim()
        ),
    );
    unsafe_text(&replaced)
}

